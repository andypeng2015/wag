func (ISA) ConversionOp(text gen.Buffer, code gen.RegCoder, oper uint16, resultType abi.Type, source values.Operand) (result values.Operand) {
	switch oper {
	case opers.Wrap:
		return opWrap(text, code, resultType, source)

	default:
		return commonConversionOp(text, code, oper, resultType, source)
	}
}

func opWrap(text gen.Buffer, code gen.RegCoder, resultType abi.Type, source values.Operand) values.Operand {
	source.Type = abi.I32 // short mov; useful zeroExt flag
	reg, zeroExt := opMaybeResultReg(text, code, source, false)
	return values.TempRegOperand(resultType, reg, zeroExt)
}

func commonConversionOp(text gen.Buffer, code gen.RegCoder, oper uint16, resultType abi.Type, source values.Operand) values.Operand {
	reg, zeroExt := opMaybeResultReg(text, code, source, false)
	// TODO: for int<->float ops: borrow source reg, allocate target reg

	switch oper {
	case opers.ExtendS:
		movsxd.opFromReg(text, 0, reg, reg)
		return values.TempRegOperand(resultType, reg, false)

	case opers.ExtendU:
		if !zeroExt {
			mov.opFromReg(text, abi.I32, reg, reg)
		}
		return values.TempRegOperand(resultType, reg, false)

	case opers.Mote:
		cvts2sSSE.opFromReg(text, source.Type, reg, reg)
		return values.TempRegOperand(resultType, reg, false)

	case opers.TruncS:
		cvttsSSE2si.opReg(text, source.Type, resultType, RegResult, reg)
		code.FreeReg(source.Type, reg)
		return values.TempRegOperand(resultType, RegResult, true)

	case opers.TruncU:
		if resultType == abi.I32 {
			cvttsSSE2si.opReg(text, source.Type, abi.I64, RegResult, reg)
		} else {
			opTruncFloatToUnsignedI64(text, code, source.Type, reg)
		}
		code.FreeReg(source.Type, reg)
		return values.TempRegOperand(resultType, RegResult, false)

	case opers.ConvertS:
		cvtsi2sSSE.opReg(text, resultType, source.Type, RegResult, reg)
		code.FreeReg(source.Type, reg)
		return values.TempRegOperand(resultType, RegResult, false)

	case opers.ConvertU:
		if source.Type == abi.I32 {
			if !zeroExt {
				mov.opFromReg(text, abi.I32, reg, reg)
			}
			cvtsi2sSSE.opReg(text, resultType, abi.I64, RegResult, reg)
		} else {
			opConvertUnsignedI64ToFloat(text, code, resultType, reg)
		}
		code.FreeReg(source.Type, reg)
		return values.TempRegOperand(resultType, RegResult, false)

	case opers.Reinterpret:
		if source.Type.Category() == abi.Int {
			movSSE.opFromReg(text, source.Type, RegResult, reg)
		} else {
			movSSE.opToReg(text, source.Type, RegResult, reg)
		}
		code.FreeReg(source.Type, reg)
		return values.TempRegOperand(resultType, RegResult, true)
	}

	panic("unknown conversion op")
}

func opTruncFloatToUnsignedI64(text gen.Buffer, code gen.Coder, sourceType abi.Type, inputReg regs.R) {
	// this algorithm is copied from code generated by gcc and clang:

	roDataAddr := code.RODataAddr()
	truncMaskAddr := gen.MaskAddr(roDataAddr, gen.MaskTruncBase, sourceType)

	movapSSE.opFromReg(text, sourceType, RegScratch, inputReg)
	subsSSE.opFromAddr(text, sourceType, RegScratch, 0, NoIndex, truncMaskAddr)
	cvttsSSE2si.opReg(text, sourceType, abi.I64, RegResult, RegScratch)
	mov.opFromAddr(text, abi.I64, RegScratch, 0, NoIndex, roDataAddr+gen.ROMask80Addr64)
	xor.opFromReg(text, abi.I64, RegScratch, RegResult)
	cvttsSSE2si.opReg(text, sourceType, abi.I64, RegResult, inputReg)
	ucomisSSE.opFromAddr(text, sourceType, inputReg, 0, NoIndex, truncMaskAddr)
	cmovae.opFromReg(text, abi.I64, RegResult, RegScratch)
}

func opConvertUnsignedI64ToFloat(text gen.Buffer, code gen.Coder, resultType abi.Type, inputReg regs.R) {
	// this algorithm is copied from code generated by gcc and clang:

	var done links.L
	var huge links.L

	// TODO: allocate target reg

	test.opFromReg(text, abi.I64, inputReg, inputReg)
	js.rel8.opStub(text)
	huge.AddSite(text.Pos())

	// max. 63-bit value
	cvtsi2sSSE.opReg(text, resultType, abi.I64, RegResult, inputReg)

	jmpRel.rel8.opStub(text)
	done.AddSite(text.Pos())

	huge.Addr = text.Pos()
	updateLocalBranches(text, &huge)

	// 64-bit value
	mov.opFromReg(text, abi.I64, RegScratch, inputReg)
	and.opImm(text, abi.I64, RegScratch, 1)
	shrImm.op(text, abi.I64, inputReg, 1)
	or.opFromReg(text, abi.I64, inputReg, RegScratch)
	cvtsi2sSSE.opReg(text, resultType, abi.I64, RegResult, inputReg)
	addsSSE.opFromReg(text, resultType, RegResult, RegResult)

	done.Addr = text.Pos()
	updateLocalBranches(text, &done)
}
